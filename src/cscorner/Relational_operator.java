package cscorner;

public class Relational_operator {

	public static void main(String[] args) {
		int x=10;
		int y=2;
		System.out.println("x>y "+(x>y));
		System.out.println("x<y "+(x<y));
		System.out.println("x=y "+(x==y));
		System.out.println("x>=y "+(x>=y));
		System.out.println("x<=y "+(x<=y));
		

	}

}
